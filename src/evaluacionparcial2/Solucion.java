    private void op2MultiplicarMatricesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_op2MultiplicarMatricesActionPerformed
        // TODO add your handling code here:
        if (this.op2DimensionFilasM2 != this.op2DimensionColM1) {
            JOptionPane.showMessageDialog(null, "Dimension de matrices incorrectas. No se puede multiplicar", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        float [][] resultado = new float[this.op2DimensionFilasM1][this.op2DimensionColM2];
        
        for (int i = 0; i < op2Vector1.length; i++) {
            for (int j = 0; j < op2Vector2[0].length; j++) {
                for (int k = 0; k < op2Vector1[0].length; k++) {
                    // aquí se multiplica la matriz
                    resultado[i][j] += op2Vector1[i][k] * op2Vector2[k][j];
                }
            }
        }
        
        System.out.println(Arrays.deepToString(resultado));
        
        this.op2ResultadoMultiplicacion.setText("");
        for (int i = 0; i < this.op2DimensionFilasM1; i++) {
            this.op2ResultadoMultiplicacion.append(Arrays.toString(resultado[i]) +  "\n");
        }
    }//GEN-LAST:event_op2MultiplicarMatricesActionPerformed

