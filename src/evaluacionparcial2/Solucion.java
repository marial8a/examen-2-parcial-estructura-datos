    private void op2GuardarValorMatriz2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_op2GuardarValorMatriz2ActionPerformed
        // TODO add your handling code here:
        String valoresMatriz2 = op2ValorColumaMatriz2.getText(); //1, 2, 3, 4
        if (valoresMatriz2.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un valor para la matriz 2", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String[] arregloValoresM2 = valoresMatriz2.split(",");// [1, 2, 3, 4]
        if (arregloValoresM2.length > this.op2DimensionColM2) {
            JOptionPane.showMessageDialog(null, "Valores superiores a la longitud de la matriz indicada\nValor Maximo " + this.op2DimensionColM2,
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int filaSelecionada = Integer.parseInt(this.op2SelectorFilaMatriz2.getSelectedItem().toString());
        for (int i = 0; i < arregloValoresM2.length; i++) { //[i] = [1, 2, 3, 4]
            this.op2Vector2[filaSelecionada][i] = Float.parseFloat(arregloValoresM2[i]); //[0][0] = [1]
        }

        this.refrescarPreviwOP2M2();
    }//GEN-LAST:event_op2GuardarValorMatriz2ActionPerformed

